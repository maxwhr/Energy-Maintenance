    @staticmethod
    def mock_ocr_result(payload: dict[str, Any]) -> dict[str, Any]:
        result = MultimodalResultNormalizer.default_ocr_result(mocked=True)
        result.update(
            {
                "text": "MOCK-ALM-22E Huawei SUN2000 inverter alarm screen text. Mocked OCR only.",
                "language": payload.get("language") or "chi_sim+eng",
                "confidence": 0.62,
                "regions": [],
                "detected_alarm_codes": ["MOCK-ALM-22E"],
                "detected_device_info": {"manufacturer": "huawei", "product_series": "SUN2000"},
                "limitations": [
                    "mocked=true; not_for_production=true.",
                    "No OCR engine or external OCR API was called.",
                    "Use this only for local workflow verification.",
                ],
                "mocked": True,
                "not_for_production": True,
            }
        )
        return result

    @staticmethod
    def normalize(capability: str, parsed_response: dict[str, Any]) -> dict[str, Any]:
        if capability == "ocr":
            return MultimodalResultNormalizer.normalize_ocr(parsed_response)
        return MultimodalResultNormalizer.normalize_multimodal(capability, parsed_response)

    @staticmethod
    def normalize_multimodal(capability: str, parsed_response: dict[str, Any]) -> dict[str, Any]:
        result = MultimodalResultNormalizer.default_multimodal_result(
            mocked=bool(parsed_response.get("mocked")),
            capability=capability,
        )
        if not result["mocked"]:
            result["limitations"] = []
        source = parsed_response.get("normalized_result") if isinstance(parsed_response.get("normalized_result"), dict) else parsed_response
        source = MultimodalResultNormalizer._merge_embedded_json(source)
        for key in result:
            if key in source:
                result[key] = source[key]
        MultimodalResultNormalizer._apply_alias(
            result,
            source,
            "visible_text",
            ("visible_message", "display_text", "recognized_text", "text"),
        )
        MultimodalResultNormalizer._apply_alias(
            result,
            source,
            "detected_alarm_codes",
            ("alarm_codes", "alarm_code", "fault_codes"),
        )
        MultimodalResultNormalizer._apply_alias(
            result,
            source,
            "detected_device_info",
            ("device_info", "nameplate_info", "equipment_info"),
        )
        MultimodalResultNormalizer._apply_alias(
            result,
            source,
            "visual_findings",
            ("visible_findings", "visible_elements", "visible_display_findings", "observations", "detected_objects"),
        )
        MultimodalResultNormalizer._apply_alias(
            result,
            source,
            "possible_fault_clues",
            ("possible_fault_signals", "fault_clues", "possible_causes"),
        )
        MultimodalResultNormalizer._apply_alias(
            result,
            source,
            "safety_risks",
            ("safety_flags", "safety_warnings", "safety_notes"),
        )
        MultimodalResultNormalizer._apply_alias(
            result,
            source,
            "recommended_next_steps",
            ("recommended_actions", "safe_follow_up_checks", "recommended_checks", "next_steps"),
        )
        MultimodalResultNormalizer._apply_alias(
            result,
            source,
            "limitations",
            ("not_observable", "warnings", "uncertainties"),
        )
        if source.get("summary") and not result.get("raw_text"):
            result["raw_text"] = str(source.get("summary"))
        if source.get("detected_objects") and not result.get("visual_findings"):
            result["visual_findings"] = source.get("detected_objects")
        if source.get("possible_fault_signals") and not result.get("possible_fault_clues"):
            result["possible_fault_clues"] = source.get("possible_fault_signals")
        for key in (
            "visible_text",
            "visual_findings",
            "possible_fault_clues",
            "safety_risks",
            "recommended_next_steps",
            "limitations",
        ):
            result[key] = MultimodalResultNormalizer._as_list(result.get(key))
        if not isinstance(result.get("detected_device_info"), dict):
            result["detected_device_info"] = {"value": str(result.get("detected_device_info") or "")}
        if result["image_type"] not in MULTIMODAL_IMAGE_TYPES:
            result["image_type"] = "unknown"
        result["confidence"] = MultimodalResultNormalizer._bounded_confidence(result.get("confidence"))
        result["mocked"] = bool(result.get("mocked"))
        result["real_external_api_used"] = bool(source.get("real_external_api_used"))
        result["needs_human_review"] = True
        result["root_cause_determined"] = False
        return result

    @staticmethod
    def normalize_ocr(parsed_response: dict[str, Any]) -> dict[str, Any]:
        result = MultimodalResultNormalizer.default_ocr_result(mocked=bool(parsed_response.get("mocked")))
        source = parsed_response.get("normalized_result") if isinstance(parsed_response.get("normalized_result"), dict) else parsed_response
        for key in result:
            if key in source:
                result[key] = source[key]
        if not result.get("text"):
            for key in ("recognized_text", "visible_text", "raw_text", "result", "content"):
                value = source.get(key)
                if isinstance(value, str) and value.strip():
                    result["text"] = value.strip()
                    break
                if isinstance(value, list):
                    result["text"] = "\n".join(str(item) for item in value if item)
                    break
        if source.get("warnings") and not result.get("limitations"):
            result["limitations"] = source.get("warnings")
        result["regions"] = MultimodalResultNormalizer._as_list(result.get("regions"))
        result["limitations"] = MultimodalResultNormalizer._as_list(result.get("limitations"))
        result["detected_alarm_codes"] = MultimodalResultNormalizer._as_list(result.get("detected_alarm_codes"))
        if not isinstance(result.get("detected_device_info"), dict):
            result["detected_device_info"] = {"value": str(result.get("detected_device_info") or "")}
        result["confidence"] = MultimodalResultNormalizer._bounded_confidence(result.get("confidence"))
        result["mocked"] = bool(result.get("mocked"))
        result["real_external_api_used"] = bool(source.get("real_external_api_used"))
        result["provider"] = source.get("provider")
        return result

    @staticmethod
    def _bounded_confidence(value: Any) -> float:
        try:
            parsed = float(value)
        except (TypeError, ValueError):
            return 0.0
        return max(0.0, min(parsed, 0.99))

    @staticmethod
    def _as_list(value: Any) -> list[Any]:
        if value is None:
            return []
        if isinstance(value, list):
            return value
        if isinstance(value, tuple):
            return list(value)
        return [value]

    @staticmethod
    def _merge_embedded_json(source: dict[str, Any]) -> dict[str, Any]:
        raw_text = source.get("raw_text")
        if not isinstance(raw_text, str) or not raw_text.strip():
            return source
        candidate = raw_text.strip()
        fenced = re.fullmatch(r"```(?:json)?\s*(.*?)\s*```", candidate, flags=re.IGNORECASE | re.DOTALL)
        if fenced:
            candidate = fenced.group(1).strip()
        try:
            parsed = json.loads(candidate)
        except json.JSONDecodeError:
            return source
        if not isinstance(parsed, dict):
            return source
        return {**source, **parsed, "raw_text": raw_text}

    @staticmethod
    def _apply_alias(
        result: dict[str, Any],
        source: dict[str, Any],
        target: str,
        aliases: tuple[str, ...],
    ) -> None:
        if MultimodalResultNormalizer._has_content(result.get(target)):
            return
        for alias in aliases:
            value = source.get(alias)
            if value not in (None, "", [], {}):
                result[target] = value
                return

    @staticmethod
    def _has_content(value: Any) -> bool:
        if isinstance(value, dict):
            return any(MultimodalResultNormalizer._has_content(item) for item in value.values())
        if isinstance(value, (list, tuple, set)):
            return any(MultimodalResultNormalizer._has_content(item) for item in value)
        return value not in (None, "")
