from __future__ import annotations

import json
import re
from typing import Any


MULTIMODAL_IMAGE_TYPES = {
    "fault_scene",
    "nameplate",
    "alarm_screen",
    "wiring",
    "document_photo",
    "general",
    "unknown",
}


class MultimodalResultNormalizer:
    @staticmethod
    def default_multimodal_result(*, mocked: bool = False, capability: str = "fault_scene_analysis") -> dict[str, Any]:
        image_type = {
            "nameplate_extract": "nameplate",
            "alarm_screen_analysis": "alarm_screen",
            "fault_scene_analysis": "fault_scene",
        }.get(capability, "general")
        return {
            "image_type": image_type,
            "summary": "",
            "visible_text": [],
            "detected_alarm_codes": [],
            "detected_device_info": {
                "manufacturer": "",
                "product_series": "",
                "model": "",
                "serial_number": "",
            },
            "visual_findings": [],
            "possible_fault_clues": [],
            "safety_risks": [],
            "recommended_next_steps": [],
            "limitations": [
                "No real external multimodal API call was performed.",
                "Machine result is auxiliary evidence and requires manual verification.",
            ],
            "raw_text": "",
            "confidence": 0.0,
            "mocked": mocked,
        }

    @staticmethod
    def default_ocr_result(*, mocked: bool = False) -> dict[str, Any]:
        return {
            "text": "",
            "language": "chi_sim+eng",
            "confidence": 0.0,
            "regions": [],
            "detected_alarm_codes": [],
            "detected_device_info": {},
            "limitations": [
                "No real OCR API call was performed.",
                "OCR result is auxiliary evidence and requires manual verification.",
            ],
            "mocked": mocked,
        }

